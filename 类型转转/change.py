#类型转换 type casting

name = "Jim Li"
age = 34
mpc = 1.6
university = True

print(type(name))
print(type(age))
print(type(mpc))
print(type(university))

age = float(age)
print(age)
print(type(age))

mpc = int(mpc)
print(mpc)
print(type(mpc))

university = str(university)
print(type(university))
print(university)